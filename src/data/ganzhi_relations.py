# === 查询函数 ===

def get_nayin(ganzhi: str) -> str:
    """获取干支的纳音"""
    return NAYIN.get(ganzhi, '')


def get_shishen(ri_gan: str, other_gan: str) -> str:
    """获取十神关系"""
    return SHISHEN.get(ri_gan, {}).get(other_gan, '')


def get_changsheng(gan: str, zhi: str) -> str:
    """获取长生状态"""
    return CHANGSHENG.get(gan, {}).get(zhi, '')


def check_chong(zhi1: str, zhi2: str) -> bool:
    """检查地支是否相冲"""

def find_events_by_gregorian(start: int, end: int) -> list:
    """按公历年范围查找历史事件"""
    return [
        {'皇极年': y, **info}
        for y, info in GUANWU_CHRONICLE.items()
        if start <= info['公历'] <= end
    ]


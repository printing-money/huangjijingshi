class InterpretationEngine:
    """卦辞解读引擎"""

    def get_interpretation(self, hexagram) -> Optional[HexagramInterpretation]:
        """获取卦象的完整解读"""
        return INTERPRETATIONS.get(hexagram.name)

    def get_yao_ci(self, hexagram, yao: int) -> Optional[str]:
        """获取特定爻的爻辞"""
        interp = INTERPRETATIONS.get(hexagram.name)
        if interp and 1 <= yao <= 6 and len(interp.yao_ci) >= yao:
            return interp.yao_ci[yao - 1]
        return None

    def interpret_chain(self, chain) -> dict:
        """解读完整卦象链"""
        result = {"layers": {}, "overall": "", "keywords": [], "nature": ""}
        layer_data = [
            ('元', chain.yuan), ('会', chain.hui), ('运', chain.yun),
            ('世', chain.shi), ('岁', chain.sui),
        ]
        all_keywords = []
        natures = []
        for label, hex_ in layer_data:
            interp = INTERPRETATIONS.get(hex_.name)
            layer_info = {
                'hexagram': f'{hex_.unicode}{hex_.name}',
                'gua_ci': interp.gua_ci if interp else '',
                'xiang_ci': interp.xiang_ci if interp else '',
                'nature': interp.nature if interp else '平',
                'keywords': interp.keywords if interp else [],
            }
            result['layers'][label] = layer_info
            if interp:
                all_keywords.extend(interp.keywords)
                natures.append(interp.nature)
        result['keywords'] = list(set(all_keywords))
        nature_scores = {'大吉': 2, '吉': 1, '平': 0, '凶': -1, '大凶': -2}
        if natures:
            avg = sum(nature_scores.get(n, 0) for n in natures) / len(natures)
            if avg > 1: result['nature'] = '大吉'
            elif avg > 0.3: result['nature'] = '吉'
            elif avg > -0.3: result['nature'] = '平'
            elif avg > -1: result['nature'] = '凶'
            else: result['nature'] = '大凶'
        sui_interp = INTERPRETATIONS.get(chain.sui.name)
        yun_interp = INTERPRETATIONS.get(chain.yun.name)
        parts = []
        if yun_interp: parts.append(f'运势大局：{yun_interp.xiang_ci}')
        if sui_interp:
            parts.append(f'流年主题：{sui_interp.gua_ci}')
            parts.append(f'象曰：{sui_interp.xiang_ci}')
        result['overall'] = '；'.join(parts) if parts else '卦辞数据待补充'
        return result

